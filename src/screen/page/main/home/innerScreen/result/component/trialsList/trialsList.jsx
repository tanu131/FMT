const TrialList = () => {
  const [currentPage, setCurrentPage] = useState(1);
  const trialsPerPage = 7;
  const totalPages = Math.ceil(trialData.length / trialsPerPage);
  const navigate = useNavigate();
  const lastIndex = currentPage * trialsPerPage;
  const firstIndex = lastIndex - trialsPerPage;
  const currentTrials = trialData.slice(firstIndex, lastIndex);

  const handleMoreInfoClick = (trial) => {
    navigate(`/category/result/study`, { state: { trial } });
  };

  return (
    <div>
      <div className="space-y-6 relative overflow-y-scroll">
        {currentTrials.map((trial) => (
          <ResultCard
            key={trial.trialId}
            trialId={trial.trialId}
            title={trial.title}
            drugName={trial.drugName}
            timeInTrial={trial.timeInTrial}
            hospitalVisits={trial.hospitalVisits}
            description={trial.description}
            onMoreInfoClick={() => handleMoreInfoClick(trial)}
          />
        ))}
      </div>
      <div className="flex justify-between items-center text-black font-semibold mt-4">
        <p>
          Showing {firstIndex + 1} to {Math.min(lastIndex, trialData.length)}{" "}
          entries out of {trialData.length}
        </p>
        <div className="flex justify-between gap-4">
          <div className="flex items-center">
            <GoArrowLeft />
            <DefaultButton
              label="PREV"
              className="px-4 py-2 rounded-md"
              onClick={() => setCurrentPage((prev) => Math.max(prev - 1, 1))}
              disabled={currentPage === 1}
            />
          </div>
          <span className="flex items-center">
            Page {currentPage} of {totalPages}
          </span>
          <div className="flex items-center">
            <DefaultButton
              label="NEXT"
              className="px-4 py-2 rounded-md"
              onClick={() =>
                setCurrentPage((prev) => Math.min(prev + 1, totalPages))
              }
              disabled={currentPage === totalPages}
            />
            <GoArrowRight />
          </div>
        </div>
      </div>
    </div>
  );
};

export default TrialList;
